    public StaffPreviousOrderLines(Staff staff, int orderIdIn) {

        loggedInStaff=staff;
        orderId = orderIdIn;
        initComponents();
        ViewUtils.setStandardSize(this);
        this.pack();
        //initializing DBManager
        DBManager db = new DBManager();
        //creating hashmap -loading zookeepers from the database
        HashMap<String, Customer> customers = db.loadCustomers();
        //initializing Order instance
        Order order = new Order();
        //looping through all the ZooKeeper records
        for(Map.Entry<String, Customer> cEntry : customers.entrySet())
        {   
            Customer customer = cEntry.getValue();
            //checking conditions - orderId
            if(customer.getOrders().containsKey(orderId))
            {   //getting the desired order
                order = customer.getOrders().get(orderId);
            }
        }
        //initialising a DefaultTableModel object
        DefaultTableModel basketModel = (DefaultTableModel)tblOrderLines.getModel();
        //for loop through each Orderline inside currentOrder, to fill up the table
        for(Map.Entry<Integer, OrderLine> olEntry : order.getOrderLines().entrySet())
        {   //initialising Orderline object to get the values
            OrderLine orderLine = olEntry.getValue();
            //adding values to the rows
            basketModel.addRow( new Object[] {
            orderLine.getProduct().getProductId(),
            orderLine.getProduct().getProductName(), "Â£" +
            String.format("%.02f", orderLine.getProduct().getPrice()),
            orderLine.getQuantity(),
            String.format("%.02f", orderLine.getLineTotal())
            });
        }
    }
