    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        lblErrorMessage.setText("");
        //getting values from the user and storing them as Strings
        String productId = txtProductID.getText();
        String productName = txtName.getText();
        String productPrice = txtPrice.getText();
        String stockLevel = txtStockLevel.getText();
        String variable = txtVariable.getText();
        //checking conditions
        if(productId.equals("") || productName.equals("") || productPrice.equals("") || stockLevel.equals("") || variable.equals(""))
        {   //displaying error message
            lblErrorMessage.setText("Error: Please Complete all Fields");
        }
        else
        {
           try 
            {   //parsing productId into an integer
                int intProductId = Integer.parseInt(productId);
                
                try 
                {   //parsing stockLevel into an integer
                    int intStockLevel = Integer.parseInt(stockLevel);
                    
                        try 
                        {   //parsing productPrice into a double
                            double doubleProductPrice = Double.parseDouble(productPrice);
                            
                               //checking what product type has been selected
                               if(product.getClass().getName().equals("models.Clothing"))
                               {
                                  //storing the special attribute
                                  String measurement = variable;
                                  //creating a Clothing object
                                  Clothing clothing = new Clothing(intProductId, productName, doubleProductPrice, intStockLevel, measurement);
                                  //calling DBManager
                                  DBManager db = new DBManager();
                                  //passing the clothing object for updating
                                  db.editProduct(clothing);
                                  lblErrorMessage.setText("Product Updated Successfully");
                               }
                               else
                               {
                                   try 
                                   {   ////parsing variable into an integer
                                       int intSize = Integer.parseInt(variable);
                                       //storing the special attribute
                                       //creating a Footwear object
                                       Footwear footwear = new Footwear(intProductId, productName, doubleProductPrice, intStockLevel, intSize);
                                       //calling DBManager
                                       DBManager db = new DBManager();
                                       //passing the footwear object for updating
                                       db.editProduct(footwear);
                                       //showing feedback message
                                       lblErrorMessage.setText("Product Updated Successfully");
                                   } 
                                   catch (Exception e) 
                                   {   //showing feedback message
                                       lblErrorMessage.setText("Error: Size should be an Integer");

                                   }
                               }
                        }
                        catch (Exception e) 
                        {   //showing feedback message
                            lblErrorMessage.setText("Error: Price should be decimal");
                        }
                }
                catch (Exception e) 
                {   //showing feedback message
                    lblErrorMessage.setText("Error: Stock Level should be an Integer");
                }
            } 
            catch (Exception e) 
            {   //showing feedback message
                lblErrorMessage.setText("Error: Product ID should be a number");
            } 
        }
        
        
        
        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

