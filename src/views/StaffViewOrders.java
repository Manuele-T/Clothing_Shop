    public StaffViewOrders(Staff staffIn) {
        loggedInStaff = staffIn;
        initComponents();
        this.pack();
        ViewUtils.setStandardSize(this);
        //initialising a DefaultTableModel object
        DefaultTableModel ordersModel = (DefaultTableModel)tblOrder.getModel();
        DBManager db = new DBManager();
        //initializing allCustomer Hashmap, through db.LoadCustomers
        HashMap<String, Customer> allCustomers= db.loadCustomers();
        //for loop, looping through our customers entries
        for(Map.Entry<String, Customer> cEntry : allCustomers.entrySet())
        {   //initializing actualCustomer object, basically one object for each customer entry
            Customer actualCustomer= cEntry.getValue();
            //for loop, looping through every customer's orders
            for(Map.Entry<Integer, Order> oEntry : actualCustomer.getOrders().entrySet())
            {   //getting values for each order
                Order actualOrder = oEntry.getValue();
                //adding rows to our table
                ordersModel.addRow(new Object[]{
                    actualCustomer.getUserName(),
                    actualOrder.getOrderId(),
                    new SimpleDateFormat("dd/MM/yyyy").format(actualOrder.getOrderDate()),
                    "Â£" + String.format("%.02f", actualOrder.getOrderTotal()),
                    actualOrder.getStatus()
                });
                }
            }
    }

